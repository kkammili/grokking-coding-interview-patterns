/**
 * Problem Statement:
 * Design a data structure that supports the following two operations:
 * - addNum(int num): Add a number to the data structure.
 * - findMedian(): Return the median of all elements so far.
 *
 * Description:
 * Use two heaps to maintain the lower and upper halves of the data stream.
 * The max-heap stores the smaller half, and the min-heap stores the larger half.
 * Balance the heaps such that their sizes differ by at most one.
 */

class MedianFinder {
    constructor() {
        this.small = new MaxHeap(); // max-heap
        this.large = new MinHeap(); // min-heap
    }

    addNum(num) {
        this.small.insert(num);
        if (this.small.size() > this.large.size() + 1) {
            this.large.insert(this.small.extractMax());
        } else if (this.large.size() > 0 && this.small.peek() > this.large.peek()) {
            this.large.insert(this.small.extractMax());
            this.small.insert(this.large.extractMin());
        }
    }

    findMedian() {
        if (this.small.size() > this.large.size()) {
            return this.small.peek();
        }
        return (this.small.peek() + this.large.peek()) / 2;
    }
}

class MinHeap {
    constructor() {
        this.heap = [];
    }

    insert(val) {
        this.heap.push(val);
        this.bubbleUp();
    }

    bubbleUp() {
        let index = this.heap.length - 1;
        while (index > 0) {
            let element = this.heap[index];
            let parentIndex = Math.floor((index - 1) / 2);
            let parent = this.heap[parentIndex];

            if (parent <= element) break;
            this.heap[index] = parent;
            this.heap[parentIndex] = element;
            index = parentIndex;
        }
    }

    extractMin() {
        const min = this.heap[0];
        const end = this.heap.pop();
        if (this.heap.length > 0) {
            this.heap[0] = end;
            this.sinkDown();
        }
        return min;
    }

    sinkDown() {
        let index = 0;
        const length = this.heap.length;
        const element = this.heap[0];

        while (true) {
            let leftChildIndex = 2 * index + 1;
            let rightChildIndex = 2 * index + 2;
            let leftChild, rightChild;
            let swap = null;

            if (leftChildIndex < length) {
                leftChild = this.heap[leftChildIndex];
                if (leftChild < element) {
                    swap = leftChildIndex;
                }
            }

            if (rightChildIndex < length) {
                rightChild = this.heap[rightChildIndex];
                if (
                    (swap === null && rightChild < element) ||
                    (swap !== null && rightChild < leftChild)
                ) {
                    swap = rightChildIndex;
                }
            }

            if (swap === null) break;
            this.heap[index] = this.heap[swap];
            this.heap[swap] = element;
            index = swap;
        }
    }

    size() {
        return this.heap.length;
    }

    peek() {
        return this.heap[0];
    }
}

